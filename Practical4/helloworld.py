# Define variable 'a' and assign it the string value "hello"
a = "hello"

# Define variable 'b' and assign it the string value "world!"
b = "world!"

# Concatenate the strings stored in 'a' and 'b' using the '+' operator
# The '+' operator joins two strings together without any space in between
print(a + b)  # Output: "helloworld!"
